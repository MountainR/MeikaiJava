public class SumAve2 {
    public static void main(String[] args){
        double x;
        double y;
        double z;

        x = 63;
        y = 18;
        z = 87;

        System.out.println("x is " + x + ".");
        System.out.println("y is " + y + ".");
        System.out.println("z is " + z + ".");
        System.out.println("Sum is " + (x+y+z) + ".");
        System.out.println("Ave is " + (x+y+z)/3 + ".");
    }

}
